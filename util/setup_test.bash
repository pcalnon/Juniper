    OFFSET="${CONDA_OFFSET}"
elif [[ ${USE_MAMBA} ]]; then
    CMD="${MAMBA_CMD}"
    OFFSET="${MAMBA_OFFSET}"
else
    echo "Borked"
    exit 1
fi


